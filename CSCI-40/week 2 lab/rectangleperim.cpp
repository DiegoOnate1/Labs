// This program will output the circumference and area
// of the circle with a given radius.

// 

#include <iostream>
using namespace std;



int main()
{


int length = 8;
int width = 3;

int area = length * width;
int perimeter = 2 * (length + width);
cout << "Area = " << area << endl;
cout << "perimeter = " << perimeter << endl;

	return 0;
}